function countStar(arr) {
    sum=0;
    count=0;
    for(var i=0;i<arr.length;i++) {
        for(var j=0;j<arr.length;j++) {
            if(arr[i][j]=='*') {
                sum++;
            }
        }
        count++
    }
    return Number.parseFloat(sum/count).toFixed(2)
}
